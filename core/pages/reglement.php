       <legend>Généralités</legend> 
        <p>
Vous venez de rejoindre la communauté de Rolkithan et nous vous remercions d’avoir choisi notre serveur. Sachez que le Staff de rokithan est là pour encadrer le fonctionnement du serveur afin d’optimiser les conditions ainsi que le confort de jeu.<br/>

En tant que membre, vous vous devez de lire la « Charte de Rolkithan ». Cette dernière a pour vocation de décrire les règles inhérentes à la vie sur notre serveur afin que chacun puisse y évoluer dans un contexte de jeu structuré, équitable et agréable.<br/>

Sont donc développées dans la Charte les règles relatives à l’utilisation de notre forum ( <a href="http://forum.rolkithan.fr">http://www.forum.rolkithan.fr </a>) mais également les règles à respecter dans le cadre du jeu. Le non-respect de ces règles pourra entraîner des sanctions allant du simple avertissement au ban définitif du serveur.<br/>

Il convient de préciser que la Charte de Rolkithan est évolutive et que nous nous réservons le droit d’en modifier le contenu.<br/>

Sachez que cette dernière servira de socle à l’ensemble de nos décisions et qu’elle apporte une réponse claire à la quasi totalité des situations rencontrées sur le serveur.<br/>
</p>
</fieldset>
<br/><br/>

<fieldset>
<legend>Réglement</legend> 
 <h3><strong style="color:orange; font-size:14px;" >//Article 1 :</strong ></h3>
<p>
- Les insultes, autres que dans un contexte bien compris entre les deux joueurs, sont INTERDITES ! Il en va de même pour les pseudos ou messages à caractère pornographique, raciste, antisémite, etc...<br/>
De plus, tout message, pseudonyme, nom de familier pouvant blesser quelqu'un de quelque façon que ce soit ou ayant rapport avec le racisme, la sexualité, ou ayant rapport avec un sujet n'étant généralement pas abordé en présence d'enfants est sanctionnable.<br/>
</p>
 <h3><strong style="color:orange; font-size:14px;" >//Article 2 :</strong ></h3>

 <p>
- Respectez les canaux du jeu. Utilisez les à bon escient pour des requêtes de marchandises, d'objet, d'instance, d'aide auprès d'autres joueurs ou de guildes. Tout débordement (flood, insultes,...) est sanctionnable. De plus il est interdit d'écrire un message tout en MAJ sur le canal world ! Vous serez sanctionné d'un ban temporaire du canal !<br/>
Sont considérés comme lieux de discussion le forum mais également les canaux de discussion mis à votre disposition au sein du jeu. En voici une liste non exhaustive :
<br/>
    * World<br/>
    * Champs de bataille<br/>
    * Groupe<br/>
    * Raid<br/>
    * Guilde<br/>
    * Dire<br/>
    * Chuchoter<br/>
    * Crier<br/>
    * etc…<br/>

Nota : ces règles sont également applicables sur Team Speak (canal Rolkithan mis gracieusement à votre disposition donc soumis à nos règles).<br/>
</p>
 <h3><strong style="color:orange; font-size:14px;" >//Article 3 :</strong ></h3>
<p>
- Si vous avez besoin d'un MJ car vous avez un problème, vous ne devez PAS flooder le canal /world, il existe pour cela un système de requêtes (le petit point d'interrogation rouge dans votre barre de menu en bas) que vous devez utiliser pour contacter un Maitre de Jeu.<br/>
</p>
 <h3><strong style="color:orange; font-size:14px;" >//Article 4 :</strong ></h3>
<p>
- En instance, après avoir tué un boss, quand on ramasse le butin, ne mettez pas "besoin" sur un objet que vous n'allez pas équiper alors que d'autres personnes en ont, elles, vraiment besoin ! Vous risqueriez de vous faire rejeter par la suite. Utilisez "cupidité".<br/>
</p>
 <h3><strong style="color:orange; font-size:14px;" >//Article 5 :</strong ></h3>
<p>
- N'insultez pas le travail du Staff, que ce soit une critique envers une intervention, une mise à jour mise en place, des quêtes ou des décorations festives. Il y a pour cela un forum qui existe et qui vous permet d'exprimer votre opinion ! Vous risqueriez de vous faire mal voir de toute la communauté, et des MJ en premier (parce que critiquer sur le canal communautaire reviendrait à dire que vous faites des remarques sur le travail de certaines personnes mais que vous n'avez même pas pris la peine d'aller lire les règles du serveur).<br/>
</p>
 <h3><strong style="color:orange; font-size:14px;" >//Article 6 :</strong ></h3>
<p>
- Il est INTERDIT d'utiliser un quelconque logiciel vous permettant de ne plus être sur le même pied d'égalité que les autres joueurs, logiciels plus communément appelés "cheats". L'utilisation de "cheats" sera sanctionnée par une fermeture pure et simple du compte sur lequel a été constaté la triche.<br/>

Les add-ons sont, eux, autorisés, tant qu'ils ne modifient pas des objets, votre niveau etc...<br/>

-De plus, les addons modifiant la couleur de votre texte sont INTERDIT et votre compte sera fermé DIRECTEMENT !<br/>
</p>
<h3><strong style="color:orange; font-size:14px;" >//Article 7 :</strong ></h3>
<p>
- Malgré toute l’attention apportée à la recherche d’éventuels bugs, il peut arriver que vous vous trouviez confronté à un bug au sein du jeu. Ces bugs peuvent prendre différentes formes : quêtes qui se valident sans avoir besoin de les faire, attirer un boss en dehors de la pièce où il se trouve, traverser un mur ou une porte normalement fermée, etc.<br/><br/>

Que faire si vous ça vous arrive ?<br/><br/>

- Ne surtout pas cacher cette information pour en profiter ou/et en faire profiter d’autres joueurs.<br/>
- Le signaler IMMÉDIATEMENT aux MJ via une requête.
Le non-respect de ces consignes vous expose à des sanctions donc soyez extrêmement vigilants.<br/>
</p>
<h3><strong style="color:orange; font-size:14px;" >//Article 8 :</strong ></h3>
<p>
- La publicité est INTERDITE, et sera sanctionnée par un ban permanent.<br/>
</p>
 <h3><strong style="color:orange; font-size:14px;" >//Article 9:</strong ></h3>
<p>
-Si vous manquez de respect à un maitre du jeu, critiquez son travail ou celui de l'équipe sans arguments, le MJ aura tous les droits de supprimer votre compte.<br/>
Les Maîtres du Jeu (MJ) sont des personnes bénévoles qui oeuvrent quotidiennement au bon fonctionnement du serveur. Ils veillent au respect de la Charte de Rolkithan, vous aident à la résolution de vos problèmes en jeu, organisent des événements originaux (Events), corrigent d’éventuels bugs, etc.<br/>
Ils ont été choisis pour leur exemplarité, leurs valeurs ainsi que pour leurs compétences personnelles.<br/>
<br/>
Il va de soit que vous leurs devez le respect comme à toute autre personne.<br/>
Ainsi, toute marque d’impolitesse ou d’irrespect à leur égard sera sévèrement réprimée.<br/>

Leur identité devant rester secrète, ils ont pour consigne de ne pas divulguer les pseudonymes de leurs personnages joueurs.<br/>
De la même manière, la divulgation d’identité d’un MJ par un joueur est passible d’un ban à vie du serveur.<br/>

La discrétion est donc de rigueur au sein de l’équipe des MJ. Ils travaillent dans l’anonymat car c’est l’assurance d’une plus grande neutralité et d’une plus grande objectivité dans les tâches qui leurs sont confiées.<br/>
</p>

 <h3><strong style="color:orange; font-size:14px;" >//Article 10:</strong ></h3>
<p>
-Lors de l'écriture d'une requête en jeu, vous devez être poli et être patient. En effet, vous devrez certaines fois attendre quelques temps avant qu'un MJ ait le temps de traiter votre requête. De plus, une requête qui concerne un problème qui peut être résolu par une simple question à la communauté, ou qui concerne un problème d'addon peut être supprimé sans avertissements.<br/>
</p>
<h3><strong style="color:orange; font-size:14px;" >//Article 11:</strong ></h3>
<p>
-Vous ne pourrez pas être remboursé de vos achats sur la boutique si votre compte est banni pour tricherie.<br/>
</p>

